package com.StudentCrud.File.system;

public class FileRecord {
	private String id;
	private String content;

	
	public FileRecord() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
